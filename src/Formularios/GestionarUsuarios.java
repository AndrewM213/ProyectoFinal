    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tablaU.getSelectedRow();
        if(filaSeleccionada >= 0){
            int resp = JOptionPane.showConfirmDialog(this, "¿Estas seguro de elimiar a ");
            if(resp == JOptionPane.YES_OPTION){
                gestion.eliminarUsuario(filaSeleccionada);
                JOptionPane.showMessageDialog(this, "Usuario Eliminado");
            }
        }else {
            JOptionPane.showMessageDialog(this, "Seleccione un usuario");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        String id = txtID.getText();
        String nombre = txtUsuario.getText();
        String contraseña = txtContraseña.getText();
        String rol = txtRol.getText();

        boolean bien = gestion.guardarUsuario(id, nombre, rol, rol);

        if(bien){
            JOptionPane.showMessageDialog(this, "Usuario Guardado");
            txtContraseña.setText("");
            txtID.setText("");
            txtUsuario.setText("");
            txtRol.setText("");
        }else {
            JOptionPane.showMessageDialog(this, "Error, ingrese bien los datos");
        }

    }//GEN-LAST:event_btnguardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int filaSeleccionada = tablaU.getSelectedRow();
        if(filaSeleccionada >= 0){
            String id = txtID.getText();
            String nombre = txtUsuario.getText();
            String contraseña = txtContraseña.getText();
            String rol = txtRol.getText();

            gestion.modificarUsuario(filaSeleccionada, id, nombre, rol, rol);
            JOptionPane.showMessageDialog(this, "Usuario Modificado");
            txtContraseña.setText("");
            txtID.setText("");
            txtUsuario.setText("");
            txtRol.setText("");
        }else {
            JOptionPane.showMessageDialog(this, "Seleccione un usuario");
        }

    }//GEN-LAST:event_btnModificarActionPerformed

